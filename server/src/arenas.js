/**
 * Parse an ASCII layout into the exact same shape generateArena() returns:
 * { tiles, wallHP, theme, spawnPoints, powerupSpots, hazards }
 * All feature positions are tile-center world coordinates.
 * Throws on wrong dimensions, unknown chars, too few spawns/powerup spots.
 */
function parseArena(layout) {
  const TS = CONFIG.TILE_SIZE;
  const center = (c, r) => ({ x: c * TS + TS / 2, y: r * TS + TS / 2 });

  if (!CONFIG.THEME_NAMES.includes(layout.theme)) {
    throw new Error(`Arena '${layout.id}': unknown theme '${layout.theme}'`);
  }
  if (!Array.isArray(layout.map) || layout.map.length !== ROWS) {
    throw new Error(`Arena '${layout.id}': map must have ${ROWS} rows`);
  }

  const tiles  = Array.from({ length: ROWS }, () => new Array(COLS).fill(TILE.FLOOR));
  const wallHP = Array.from({ length: ROWS }, () => new Array(COLS).fill(0));

  const spawnPoints  = [];
  const powerupSpots = [];
  const hazards = { mines: [], turrets: [], blackholes: [], gravity: [], wormholes: [], wave: layout.wave || null };

  for (let r = 0; r < ROWS; r++) {
    const row = layout.map[r];
    if (typeof row !== 'string' || row.length !== COLS) {
      throw new Error(`Arena '${layout.id}': row ${r} must be ${COLS} chars`);
    }
    for (let c = 0; c < COLS; c++) {
      const ch = row[c];
      if (ch in CHAR_TILES) {
        tiles[r][c] = CHAR_TILES[ch];
        if (tiles[r][c] === TILE.WALL_DEST) wallHP[r][c] = CONFIG.WALL_DEST_HP;
      } else if (FEATURE_CHARS.has(ch)) {
        tiles[r][c] = TILE.FLOOR;
        const pos = center(c, r);
        if (ch === 'S') spawnPoints.push(pos);
        else if (ch === 'P') powerupSpots.push(pos);
        else if (ch === 'M') hazards.mines.push(pos);
        else if (ch === 'T') hazards.turrets.push({ type: 'missile', ...pos });
        else if (ch === 'O') hazards.turrets.push({ type: 'mortar', ...pos });
        else if (ch === 'B') hazards.blackholes.push(pos);
        else if (ch === '<') hazards.gravity.push({ ...pos, dx: -1, dy: 0 });
        else if (ch === '>') hazards.gravity.push({ ...pos, dx: 1, dy: 0 });
        else if (ch === '^') hazards.gravity.push({ ...pos, dx: 0, dy: -1 });
        else if (ch === 'v') hazards.gravity.push({ ...pos, dx: 0, dy: 1 });
        else if (ch === '1' || ch === '2') hazards.wormholes.push({ id: ch, ...pos });
      } else {
        throw new Error(`Arena '${layout.id}': unknown char '${ch}' at ${r},${c}`);
      }
    }
  }

  // Defensive: force the outer border to solid walls no matter what
  for (let c = 0; c < COLS; c++) {
    tiles[0][c]      = TILE.WALL_SOLID;
    tiles[ROWS-1][c] = TILE.WALL_SOLID;
  }
  for (let r = 0; r < ROWS; r++) {
    tiles[r][0]      = TILE.WALL_SOLID;
    tiles[r][COLS-1] = TILE.WALL_SOLID;
  }

  if (spawnPoints.length < 4) {
    throw new Error(`Arena '${layout.id}': needs at least 4 spawn points (S)`);
  }
  if (powerupSpots.length < 1) {
    throw new Error(`Arena '${layout.id}': needs at least 1 powerup spot (P)`);
  }

  return { tiles, wallHP, theme: layout.theme, spawnPoints, powerupSpots, hazards };
}

/**
 * Resolve an arena by id; 'random' (or anything falsy) → procedural arena.
 * Throws on unknown id.
 */
function getArena(idOrRandom) {
  if (!idOrRandom || idOrRandom === 'random') {
    return generateArena();
  }
  const layout = LAYOUTS.find(l => l.id === idOrRandom);
  if (!layout) throw new Error(`Unknown arena id: '${idOrRandom}'`);
  return parseArena(layout);
}

/**
 * Arena metadata for UI pickers: [{ id, name, difficulty, theme }]
 */
function arenaList() {
  return LAYOUTS.map(({ id, name, difficulty, theme }) => ({ id, name, difficulty, theme }));
}

module.exports = { LAYOUTS, parseArena, getArena, arenaList };
